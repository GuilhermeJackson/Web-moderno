const a = 7
let b = 3

b += a  // b = b + a    Acrescentar o valor de 'a' a variavel 'b'
console.log('b = b + a =', b)

b -= 4  // b = b - 4
console.log('b = b - a =', b)

b *= a  // b = b * a
console.log('b = b * a =', b)

b /= a  // b = b / a
console.log('b = b / a =', b)

b %= 2  // b = b % 2        % é o módulo que se o resultado for par é 0 e se impar é 1
console.log('b = b % 2 =', b)